package com.diquest.openapi.google.response.entity;

public class RunTimeBadge {

    private String imageUrl;
    private String badgeText;
    private String badgeIconId;

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getBadgeText() {
        return badgeText;
    }

    public void setBadgeText(String badgeText) {
        this.badgeText = badgeText;
    }

    public String getBadgeIconId() {
        return badgeIconId;
    }

    public void setBadgeIconId(String badgeIconId) {
        this.badgeIconId = badgeIconId;
    }
}
